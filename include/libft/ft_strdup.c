#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	i;
	char			*str;

	str = (void *)malloc((ft_strlen(s1) + 1) * sizeof(*s1));
	if (!str)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
